#!/usr/bin/env python3
"""
Build the deployable static site (`site/`) from the Claude Design export.

The design export is a multi-page React artifact that renders client-side via
Babel-in-browser. This script migrates it into a clean, self-contained static
site:

  * keeps the *real* pages (home, bio, contact, 5 project pages) and the JSX
    component graph they import; drops design-tool exploration artifacts
    (Nav Variants, Portfolio Explorations, design-canvas, directions/,
    site_terminal_backup, project.jsx, navvariants) and the unused
    screenshots/ + uploads/ originals;
  * renames the awkward "Roger Winter - Portfolio.html" -> index.html and sets
    HOME_FILE -> "/" (the export shipped HOME_FILE with an em-dash that does not
    match the hyphen in the real filename, so every nav/back link would 404 as a
    static site);
  * lowercases Bio.html/Contact.html -> bio.html/contact.html and fixes the few
    cross-references;
  * rewrites the three unpkg CDN <script> tags to self-hosted /vendor/* (React
    switched dev->prod min; semantically identical render, smaller + no console
    noise; Babel standalone kept verbatim);
  * injects per-page SEO/OpenGraph/Twitter meta + canonical + favicon;
  * emits favicon.svg, robots.txt, sitemap.xml.

Input : SRC (extracted design export)         -- not committed
Output: DST (committed, deployable static site)

Re-runnable and idempotent. Vendor files are downloaded by a separate step
(scripts/fetch-vendor.sh) so this script needs no network.
"""
