def die(msg):
    print("BUILD ERROR:", msg); sys.exit(1)

def reset_dst():
    if os.path.isdir(DST):
        shutil.rmtree(DST)
    os.makedirs(os.path.join(DST, "portfolio"))
    os.makedirs(os.path.join(DST, "vendor"), exist_ok=True)

def copy_jsx():
    for f in JSX_ROOT:
        shutil.copy2(os.path.join(SRC, f), os.path.join(DST, f))
    for f in JSX_PORTFOLIO:
        s = os.path.join(SRC, "portfolio", f)
        if not os.path.exists(s):
            die(f"missing expected jsx: {s}")
        shutil.copy2(s, os.path.join(DST, "portfolio", f))
    # assets
    shutil.copytree(os.path.join(SRC, "portfolio", "assets"),
                    os.path.join(DST, "portfolio", "assets"))

def patch_shell_and_bio():
    # shell.jsx: HOME_FILE -> "/", lowercase Bio/Contact nav targets
    p = os.path.join(DST, "portfolio", "shell.jsx")
    t = open(p, encoding="utf-8").read()
    t2 = re.sub(r'HOME_FILE\s*=\s*"[^"]*"', 'HOME_FILE = "/"', t, count=1)
    if t2 == t: die("shell.jsx: HOME_FILE assignment not found")
    t2 = t2.replace('href:"Bio.html"', 'href:"bio.html"')
    t2 = t2.replace('href="Contact.html"', 'href="contact.html"')
    open(p, "w", encoding="utf-8").write(t2)
    # bio.jsx: Contact.html -> contact.html
    p = os.path.join(DST, "portfolio", "bio.jsx")
    t = open(p, encoding="utf-8").read()
    open(p, "w", encoding="utf-8").write(t.replace('href="Contact.html"', 'href="contact.html"'))

VENDOR_SUBS = [
    (re.compile(r'<script[^>]*\breact-dom\.development\.js\b[^>]*>\s*</script>'),
     '<script src="/vendor/react-dom.production.min.js"></script>'),
    (re.compile(r'<script[^>]*\breact\.development\.js\b[^>]*>\s*</script>'),
     '<script src="/vendor/react.production.min.js"></script>'),
    (re.compile(r'<script[^>]*\bbabel\.min\.js\b[^>]*>\s*</script>'),
     '<script src="/vendor/babel.min.js"></script>'),
]

def build_meta_block(out_name, title):
    m = META[out_name]
    url = CANON + "/" + ("" if out_name == "index.html" else out_name)
    e = lambda s: html.escape(s, quote=True)
    img_abs = (CANON + m["img"]) if m["img"] else None
    L = [
        f'<meta name="description" content="{e(m["desc"])}" />',
        f'<link rel="canonical" href="{e(url)}" />',
        '<link rel="icon" type="image/svg+xml" href="/favicon.svg" />',
        '<meta name="theme-color" content="#0b0d0c" />',
        f'<meta name="author" content="Roger Winter" />',
        f'<meta property="og:type" content="{m["type"]}" />',
        '<meta property="og:site_name" content="Roger Winter" />',
        f'<meta property="og:title" content="{e(title)}" />',
        f'<meta property="og:description" content="{e(m["desc"])}" />',
        f'<meta property="og:url" content="{e(url)}" />',
    ]
    if img_abs:
        L.append(f'<meta property="og:image" content="{e(img_abs)}" />')
        if m.get("img_type"):
            L.append(f'<meta property="og:image:type" content="{m["img_type"]}" />')
        if m.get("img_w") and m.get("img_h"):
            L.append(f'<meta property="og:image:width" content="{m["img_w"]}" />')
            L.append(f'<meta property="og:image:height" content="{m["img_h"]}" />')
        if m.get("img_alt"):
            L.append(f'<meta property="og:image:alt" content="{e(m["img_alt"])}" />')
    L.append(f'<meta name="twitter:card" content="{"summary_large_image" if img_abs else "summary"}" />')
    L.append(f'<meta name="twitter:title" content="{e(title)}" />')
    L.append(f'<meta name="twitter:description" content="{e(m["desc"])}" />')
    if img_abs:
        L.append(f'<meta name="twitter:image" content="{e(img_abs)}" />')
        if m.get("img_alt"):
            L.append(f'<meta name="twitter:image:alt" content="{e(m["img_alt"])}" />')
    return "\n" + "\n".join(L) + "\n"

def transform_pages():
    for src_name, out_name in PAGES.items():
        sp = os.path.join(SRC, src_name)
        if not os.path.exists(sp): die(f"missing page: {sp}")
        h = open(sp, encoding="utf-8").read()
        # vendor swap (assert each happened exactly once)
        for rx, repl in VENDOR_SUBS:
            h, n = rx.subn(repl, h)
            if n != 1:
                die(f"{src_name}: expected 1 vendor swap for {repl!r}, did {n}")
        # title for og
        mt = re.search(r"<title>(.*?)</title>", h, re.S)
        title = mt.group(1).strip() if mt else "Roger Winter"
        # inject meta before </head>
        if "</head>" not in h: die(f"{src_name}: no </head>")
        h = h.replace("</head>", build_meta_block(out_name, title) + "</head>", 1)
        open(os.path.join(DST, out_name), "w", encoding="utf-8").write(h)

def emit_extras():
    open(os.path.join(DST, "favicon.svg"), "w", encoding="utf-8").write(FAVICON)
    open(os.path.join(DST, "robots.txt"), "w", encoding="utf-8").write(
        "User-agent: *\nAllow: /\n\nSitemap: %s/sitemap.xml\n" % CANON)
    urls = []
    for out_name in PAGES.values():
        loc = CANON + "/" + ("" if out_name == "index.html" else out_name)
        urls.append(f"  <url><loc>{html.escape(loc)}</loc></url>")
    open(os.path.join(DST, "sitemap.xml"), "w", encoding="utf-8").write(
        '<?xml version="1.0" encoding="UTF-8"?>\n'
        '<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">\n'
        + "\n".join(urls) + "\n</urlset>\n")

def verify_links():
    """Catch broken inter-page <a href="*.html"> links that curl can't see
    (they're rendered client-side from JSX)."""
    present = set(os.listdir(DST))
    bad = []
    for root, _, files in os.walk(os.path.join(DST, "portfolio")):
        for f in files:
            if not f.endswith(".jsx"): continue
            t = open(os.path.join(root, f), encoding="utf-8").read()
            for href in re.findall(r'href="([^"#{][^"]*\.html)"', t):
                if href not in present:
                    bad.append((f, href))
    # also resolve project-${id}.html for known ids
    ids = ["sheet-llm","onestreamer","price-games","pricey","multilingual-seo"]
    for i in ids:
        if f"project-{i}.html" not in present:
            bad.append(("data.jsx", f"project-{i}.html"))
    if bad:
        die("broken static html links: " + repr(bad))

def main():
    reset_dst()
    copy_jsx()
    patch_shell_and_bio()
    transform_pages()
    emit_extras()
    verify_links()
    # report
    total = 0
    for root,_,files in os.walk(DST):
        for f in files: total += os.path.getsize(os.path.join(root,f))
    print("OK: built", DST)
    print("  pages:", ", ".join(sorted(PAGES.values())))
    print("  jsx  :", len(JSX_ROOT)+len(JSX_PORTFOLIO), "components")
    print("  size : %.1f MB (excl. vendor downloads)" % (total/1e6))

if __name__ == "__main__":
    main()
